/**
 * 标准化地区名称
 * @param {string} location - 原始地区文本
 * @returns {string|null} 标准化地区或null
 */
export function normalizeLocation(location) {
  if (!location) return null;

  const trimmed = location.trim();

  // 直接匹配
  if (LOCATION_MAP[trimmed]) {
    return LOCATION_MAP[trimmed];
  }

  // 前缀匹配
  for (const [abbr, full] of Object.entries(LOCATION_MAP)) {
    if (trimmed.startsWith(abbr) || trimmed.includes(abbr)) {
      return full;
    }
  }

  // 未知地区，返回原始值
  return trimmed || null;
}

